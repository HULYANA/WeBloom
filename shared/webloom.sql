DROP SCHEMA IF EXISTS webloom;
CREATE SCHEMA webloom;

USE webloom;

CREATE TABLE users(
userID int (20) UNIQUE,
username varchar(20) UNIQUE NOT NULL,
pass varchar(20) NOT NULL,
email varchar(20) UNIQUE NOT NULL,
lastname varchar(20) NOT NULL,
firstname varchar(20) NOT NULL,
gender varchar(20) NOT NULL,
role varchar(20) NOT NULL,

PRIMARY KEY (userID)
)ENGINE = INNODB;

CREATE TABLE programs(
programID int (20) UNIQUE,
programName varchar(50) NOT NULL,

PRIMARY KEY (programID)
)ENGINE = INNODB;

CREATE TABLE category (
categoryID INT NOT NULL UNIQUE,
categoryName varchar(100) NOT NULL,

PRIMARY KEY (categoryID)
)ENGINE = INNODB;


CREATE TABLE questions (
questionID int NOT NULL UNIQUE AUTO_INCREMENT,
categoryID int,
questionText varchar(500) NOT NULL,
questionType varchar(100) NOT NULL,

PRIMARY KEY (questionID),
FOREIGN KEY (categoryID) REFERENCES category(categoryID) ON DELETE CASCADE
)ENGINE = INNODB;

CREATE TABLE choices(
optionsID int NOT NULL AUTO_INCREMENT,
choice varchar(100),
questionID int,

PRIMARY KEY (optionsID),
FOREIGN KEY (questionID) REFERENCES questions(questionID) ON DELETE CASCADE
)ENGINE = INNODB;

CREATE TABLE questionnaires(
questionnaireID int(11) NOT NULL,
title varchar(100) NOT NULL,
questionID int NOT NULL, 
programID int NOT NULL,
evalautionPeriod varchar(50) NOT NULL,
publicationStatus varchar(50) NOT NULL,
datePublished date DEFAULT NULL,
expirationDate date DEFAULT NULL,

PRIMARY KEY (questionnaireID, questionID),
FOREIGN KEY (questionID) REFERENCES questions(questionID) ON DELETE CASCADE
)ENGINE = INNODB;

CREATE TABLE students(
studentID int NOT NULL UNIQUE,
userID int,
programID int,

PRIMARY KEY(studentID)
)ENGINE = INNODB;

CREATE TABLE responses(
responseID int NOT NULL AUTO_INCREMENT,
studentID int NOT NULL,
questionnaireID int NOT NULL,
responseDate date,

PRIMARY KEY (responseID),
FOREIGN KEY (questionnaireID) REFERENCES questionnaires(questionnaireID) ON DELETE CASCADE, 
FOREIGN KEY (studentID) REFERENCES students(studentID) ON DELETE CASCADE
)ENGINE = INNODB;

CREATE TABLE responseAnswer(
responseID int,
answer varchar(100),

FOREIGN KEY (responseID) REFERENCES responses(responseID) ON DELETE CASCADE
)ENGINE = INNODB;

